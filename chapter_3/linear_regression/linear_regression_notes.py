#To measure the success of the learning algorithm, 
# you’ll need to understand two important concepts: 
# variance and bias.  
# • Variance is how sensitive a prediction is to what
#  training set was used. Ideally, how we choose 
# the training set shouldn’t matter – meaning a 
# lower variance is desired.  

# • Bias is the strength of assumptions made about
#  the training dataset. Making too many ass. makes it hard 
# to generalize, so we prefer a low Bias as well.

# Concretely, the variance of a model is a measure of
#  how badly the responses fluctuate, and the bias is 
# a measure of how badly the response is offset from
#  the ground-truth.
